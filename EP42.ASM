DSEG 	SEGMENT
DATA	DB 		0AH,1,3
DSEG	ENDS
CSEG 	SEGMENT
		ASSUME 	CS:CSEG,DS:DSEG
START:	MOV 	AX,DSEG
		MOV 	DS,AX
		XOR 	AX,AX
		MOV 	AL,DATA
		AND 	AL,AL	;置标志
		JNS 	PLUS	;正转
		TEST 	DATA+1,80H
		JNS 	UNSAME
		TEST 	DATA+2,80H
		JNS 	UNSAME
		JS 		NSAME
UNSAME:	MOV 	DL,'*'
		MOV 	AH,02H
		INT 	21H
		JMP 	END1
PLUS:	TEST 	DATA+1,80H
		JS 		UNSAME
		TEST 	DATA+2,80H
		JS 		UNSAME
		JMP 	PSAME
PSAME:	MOV 	DL,'+'
		MOV 	AH,02H
		INT 	21H
		JMP 	END1
NSAME:	MOV 	DL,'-'
		MOV 	AH,02H
		INT 	21H
		JMP 	END1
END1:	MOV 	AH,4CH
		INT 	21H
CSEG 	ENDS
		END 	START

