DSEG 	SEGMENT
DATAX	DB		127
DATAY 	DB 		127
HTAB	DB		'0123456789ABCDEF'
DSEG 	ENDS
CSEG 	SEGMENT
		ASSUME 	CS:CSEG,DS:DSEG
START:	MOV 	AX,DSEG
		MOV 	DS,AX
		MOV  	CH,DATAX
		MOV 	CL,DATAY
		MOV 	AH,01H	;输入字符
		INT 	21H
		MOV 	BH,'+'
		CMP 	BH,AL
		JE 		PADD
		MOV 	BH,'-'
		CMP 	BH,AL
		JE 		PSUB
		MOV 	BH,'*'
		CMP 	BH,AL
		JE 		PMUL
		MOV 	BH,'/'
		CMP 	BH,AL
		JE 		PDIV
		MOV 	DL,AL
		JMP 	PRINT
PADD:	XOR		AX,AX
		MOV 	AL,CH
		XOR 	CH,CH
		ADD  	AX,CX
		JMP 	PRINT
PSUB:	XOR 	AX,AX
		MOV 	AL,CH
		XOR 	CH,CH
		SUB 	AX,CX
		JMP 	PRINT
PMUL:	MOV 	AL,CH
		XOR 	AH,AH
		IMUL 	CL
		JMP 	PRINT
PDIV:	XOR 	AX,AX
		MOV 	AL,CH
		IDIV 	CL
		JMP 	PRINT
PRINT:	MOV 	DX,AX
		MOV 	AL,DH
		MOV 	CH,DH
		MOV 	DH,DL
		MOV 	CL,4
		SHR 	AL,CL
		LEA 	BX,HTAB
		XLAT 
		MOV  	DL,AL
		MOV 	AH,02H
		INT 	21H
		AND 	CH,0FH
		MOV 	AL,CH
		XLAT 	
		MOV 	DL,AL
		MOV 	AH,02H
		INT 	21H
		MOV 	AL,DH
		MOV 	CH,DH
		MOV 	CL,4
		SHR 	AL,CL
		LEA 	BX,HTAB
		XLAT 
		MOV  	DL,AL
		MOV 	AH,02H
		INT 	21H
		AND 	CH,0FH
		MOV 	AL,CH
		XLAT 	
		MOV 	DL,AL
		MOV 	AH,02H
		INT 	21H
		MOV 	DL,'H'
		MOV 	AH,02H
		INT 	21H
		MOV 	AH,4CH
		INT 	21H
CSEG 	ENDS 	
		END 	START