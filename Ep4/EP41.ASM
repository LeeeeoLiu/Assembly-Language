DSEG	SEGMENT
DATA	DB		2,3,0AH
HTAB 	DB		'0123456789ABCDEF'
DSEG 	ENDS
CSEG	SEGMENT
		ASSUME	CS:CSEG,DS:DSEG
START:	MOV 	AX,DSEG	
		MOV 	DS,AX
		LEA 	BX,DATA
		MOV 	CH,[BX]
		MOV 	CL,[BX+1]
		MOV 	DH,[BX+2]
		CMP 	CH,CL
		JAE 	NEXT1
		CMP 	CH,DH
		JAE 	NEXT3
		CMP 	CL,DH
		JAE 	NEXT4
		MOV 	CL,CH
		MOV 	CH,DH
		MOV 	DH,CL
		JMP 	OUTPUT
NEXT1:	CMP 	CL,DH
		JAE 	OUTPUT
		CMP 	CH,DH
		JAE 	NEXT2
NEXT2:	MOV 	DH,CL
		JMP 	OUTPUT
NEXT3:	MOV		CH,CL
		JMP 	OUTPUT
NEXT4:	MOV 	DH,CH
		MOV 	CH,CL
		JMP 	OUTPUT
OUTPUT:	LEA  	BX,HTAB
		MOV 	AL,CH
		XLAT 
		MOV 	DL,AL
		MOV 	AH,02H
		INT 	21H
		MOV 	DL,','
		MOV 	AH,02H
		INT 	21H
		MOV 	AL,DH
		XLAT 
		MOV  	DL,AL
		MOV 	AH,02H
		INT 	21H
		JMP 	DONE
DONE:	MOV 	AH,4CH
		INT 	21H
CSEG	ENDS
		END 	START