CSEG 	SEGMENT
		ASSUME	CS:CSEG,SS:SSEG
DISASC 	PROC
		PUSH 	AX
		MOV 	AH,14
		INT 	10H
		POP 	AX
		RET
DISASC 	ENDP
HTOA 	PROC 
		AND 	AL,0FH
		CMP 	AL,10
		JC 		HTOA1
		ADD 	AL,07
HTOA1:	ADD 	AL,30H
		RET 
HTOA 	ENDP
BHTOA 	PROC
		PUSH 	CX
		MOV 	CH,AL
		MOV 	CL,04
		SHR 	AL,CL
		CALL 	HTOA
		MOV 	AH,AL
		MOV 	AL,CH
		CALL 	HTOA
		POP 	CX
		RET
BHTOA	ENDP
DISAL 	PROC
		PUSH 	AX
		CALL 	BHTOA
		XCHG 	AH,AL
		CALL 	DISASC
		MOV 	AL,AH
		CALL 	DISASC
		POP 	AX
		RET 
DISAL 	ENDP
BIOS 	PROC 	FAR
		PUSH 	DS
		XOR 	AX,AX
		PUSH 	AX
AGAIN:	MOV 	AH,0
		INT 	16H
		PUSH 	AX
		CALL 	DISAL
		MOV 	AL,'-'
		CALL 	DISASC
		MOV 	AL,AH
		CALL 	DISAL
		MOV 	AL,' '
		CALL 	DISASC
		POP 	AX
		CMP 	AL,03H
		JNZ 	AGAIN
		RET 
BIOS 	ENDP
CSEG	ENDS
		END 	BIOS