		MOV 	DS,AX
		XOR 	CX,CX
		MOV 	CL,CNT
		LEA 	DI,TEXT
		LEA 	SI,KEY
		LEA 	BX,RLT
		MOV 	DL,3
GO:		MOV 	AH,[DI]
		MOV 	AL,[SI]
		XOR 	AH,AL
		MOV 	[BX],AH
		INC 	BX
		INC 	DI
		INC 	SI
		CMP 	SI,DX
		JE 		RESET
		JMP 	GOON
RESET:	SUB		SI,3		
