DSEG 	SEGMENT
DATAX	DB		4
DATAY 	DB 		-2
HTAB	DB		'0123456789ABCDEF'
DSEG 	ENDS
CSEG 	SEGMENT
		ASSUME 	CS:CSEG,DS:DSEG
START:	MOV 	AX,DSEG
		MOV 	DS,AX
		MOV  	CH,DATAX
		MOV 	CL,DATAY
		MOV 	AH,01H	;输入字符
		INT 	21H
		MOV 	BH,'+'
		CMP 	BH,AL
		JE 		PADD
		MOV 	BH,'-'
		CMP 	BH,AL
		JE 		PSUB
		MOV 	BH,'*'
		CMP 	BH,AL
		JE 		PMUL
		MOV 	BH,'/'
		CMP 	BH,AL
		JE 		PDIV
		MOV 	DL,AL
		JMP 	PRINT
PADD:	ADD 	CH,CL
		MOV 	DL,CH
		JMP 	PRINT
PSUB:	SUB 	CH,CL
		MOV 	DL,CH
		JMP 	PRINT
PMUL:	MOV 	AL,CH
		IMUL 	CL
		MOV 	DL,AL
		JMP 	PRINT
PDIV:	XOR 	AX,AX
		MOV 	AL,CH
		IDIV 	CL
		MOV 	DL,AL
		JMP 	PRINT
PRINT:	
		MOV 	AL,DL
		MOV 	CH,DL
		MOV 	CL,4
		SHR 	AL,CL
		LEA 	BX,HTAB
		XLAT 
		MOV  	DL,AL
		MOV 	AH,02H
		INT 	21H
		AND 	CH,0FH
		MOV 	AL,CH
		XLAT 	
		MOV 	DL,AL
		MOV 	AH,02H
		INT 	21H
		MOV 	DL,'H'
		MOV 	AH,02H
		INT 	21H
		MOV 	AH,4CH
		INT 	21H
CSEG 	ENDS 	
		END 	START